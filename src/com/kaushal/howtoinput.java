package com.kaushal;

import java.util.Scanner;

public class howtoinput {
    public static void main(String[] args) {
        Scanner nigga = new Scanner(System.in);
        System.out.println(nigga.nextLine());
    }
}
/* Basically what you are trying to do here is to learn all the ways
   to input from the keyboard to the program.
   Scanner: This is a class in the java.util package used to take input from various input sources,
   including the keyboard (System.in).
   new Scanner(System.in): This creates a new Scanner object that reads input from the standard input stream,
   which is typically the keyboard.
   nigga: This is the name of the variable that is assigned to the new Scanner object.

 */